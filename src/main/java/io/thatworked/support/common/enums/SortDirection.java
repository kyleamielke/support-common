package io.thatworked.support.common.enums;

/**
 * Standard sort direction enum.
 * Use for all sorting operations across services.
 */
public enum SortDirection {
    /**
     * Ascending order (A-Z, 0-9, oldest to newest).
     */
    ASC,
    
    /**
     * Descending order (Z-A, 9-0, newest to oldest).
     */
    DESC
}